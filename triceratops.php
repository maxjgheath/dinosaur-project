                Triceratops' horns – two long spikes above the eyes and a shorter protrusion from the nose – are
                easily its most recognizable feature, but the understanding of them has changed almost as much over
                time as the understanding of the dinosaur itself. Originally, the orthodox view in paleontology
                held that the dinosaur's horns were defensive weaponry used to protect the creature from T-Rex
                or other predators, but since then, many scientists have come to believe that the horns played a
                more complex role in courtship and were used by the dinosaur to distinguish members of its own
                species from other species in the Triceratops genus.
